# Abstraction

# Abstraction in python is defined as a process of handling complexity 
# by hiding unnecessary information from the user. This is one of the 
# core concepts of object-oriented programming (OOP) languages.

# For example, people do not think of a car as a set of thousands of individual 
# parts. Instead they see it as a well-defined object with its own unique 
# behavior. This abstraction allows people to use a car to drive without 
# knowing the complexity of the parts that form the car. They can ignore 
# the details of how the engine transmission, and braking systems work. 
# Instead, they are free to utilize the object as a whole.

